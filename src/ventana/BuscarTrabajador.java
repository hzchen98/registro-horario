    private void buttonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBuscarActionPerformed
        // TODO add your handling code here:
        ArrayList<Trabajador> listaBuscar = new ArrayList<>();
        String datoIntroducido = null;

        modeloTablaBusquedas = (DefaultTableModel) tablaBusqueda.getModel();
        modeloTablaBusquedas.setRowCount(0);
        if (!inputBuscar.getText().equals("")) {

            switch (comboBuscarPor.getSelectedIndex()) {
                case 0:
                    listaBuscar = Trabajador.buscarListaNombre(listaTrabajador, inputBuscar.getText(), this);
                    break;
                case 1:
                    listaBuscar = Trabajador.buscarListaApellido(listaTrabajador, inputBuscar.getText(), this);
                    break;
                case 2:
                    listaBuscar = Trabajador.buscarListaDni(listaTrabajador, inputBuscar.getText(), this);
                    break;
            };

            if (listaBuscar.size() != 0) {
                for (Trabajador t : listaBuscar) {
                    modeloTablaBusquedas.addRow(new Object[]{t.getDni(), t.getNombre(), t.getApellidos()});
                }
                tablaBusqueda.setModel(modeloTablaBusquedas);
            } else {
                JOptionPane.showMessageDialog(this, "No se ha encontrado ninguna coincidencia", "No hay coincidencia", JOptionPane.DEFAULT_OPTION);
            }
        }
    }//GEN-LAST:event_buttonBuscarActionPerformed

