    private void buttonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSeleccionarActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tablaBusqueda.getSelectedRow();
        if (filaSeleccionada != -1) {

            principal.setPos(posTrabajador.get(tablaBusqueda.getSelectedRow()));
            principal.visListaTrabajador();
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione la fila deseada de los datos del trabajador!", "Seleccionar trabajador", JOptionPane.DEFAULT_OPTION);
        }
    }//GEN-LAST:event_buttonSeleccionarActionPerformed

